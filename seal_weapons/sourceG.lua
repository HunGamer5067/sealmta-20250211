function getWeaponClothesshopData(itemId)
	local weaponModel = itemWeaponModel[itemId]
	local weaponIndex = false

	for index, weapon in pairs(customWeaponContainer) do
		if weapon.modelId == weaponModel then
			weaponIndex = index
			break
		end
	end

	if weaponIndex then
		return exports.seal_items:getItemName(itemId), customWeaponContainer[weaponIndex].boneId, customWeaponContainer[weaponIndex].clothHide, weaponModel
	end
end

